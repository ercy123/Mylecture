package java0520;

import java.util.Scanner;

public class Floor_Switchcase {

	public static void main(String[] args) {
		Scanner sc= new Scanner(System.in);
		
	//	int floor = sc.nextInt();
		String floor = sc.next();
		
		switch(floor)
		{
		case "1類":
			System.out.println("1類 擒措");
			break;
		case "2類":
			System.out.println("2類 薑⑽諼婁");
			break;
		case "3類":
			System.out.println("3類 Я睡婁");
			break;
		case "4類":
			System.out.println("4類 纂婁");
			break;
		case "5類":
			System.out.println("5類 ⑦蝶贗毀");
			break;
			
			
		
		}

	}

}
