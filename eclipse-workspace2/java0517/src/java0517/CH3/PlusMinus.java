package java0517.CH3;

public class PlusMinus {

	public static void main(String[] args) {
		int num = 10;
		
		System.out.println(+num);
		System.out.println(-num);
		System.out.println(num);
		
		num = -num;
		System.out.println(num);
		
		
		byte b = 3;
		byte c = 2;		
		
		byte d = -2;
		System.out.println(d);
		System.out.println(-d);		
		
		System.out.println(b);
		System.out.println(-b);
		System.out.println(-b*-b);
		System.out.println(b/c);		
		System.out.println(b%c);		

		
		
		
		
	}

}
